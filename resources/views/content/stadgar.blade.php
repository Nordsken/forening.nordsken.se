            <h2>Stadgar</h2>

            <h4>§1 Föreningens namn</h4>
            Föreningens namn är “Nordskens Vänner”.

            <h4>§2 Föreningens säte</h4>
            Föreningen har sitt säte i Skellefteå.

            <h4>§3 Föreningsform</h4>
            Föreningen är en ideell förening. Föreningen är ansluten till Sveriges roll- och konfliktspelsförbund.

            <h4>§4 Syfte</h4>
            Föreningens syfte är främst att främja datorspelande genom att arrangera LAN-partyn.

            <h4>§5 Oberoende</h4>
            Föreningen är religiöst och politiskt obunden.

            <h4>§6 Verksamhetsår</h4>
            Verksamhetsåret är 1 januari till 31 december.

            <h4>§7 Medlemmar</h4>
            Som medlem antas intresserad som godkänner dessa stadgar och aktivt tar ställning för ett medlemskap genom att årligen betala föreningens medlemsavgift eller, om medlemskapet är gratis, årligen göra en skriftlig anmälan till föreningen.<br>
            Avgiftens storlek beslutas på årsmötet. Årsmötet kan besluta att det är gratis att vara medlem.<br>
            En medlem som allvarligt skadar föreningen kan avstängas av styrelsen. Avstängd medlem måste diskuteras på nästa årsmöte. Antingen så upphävs då avstängningen eller så utesluts medlemmen. Styrelsen och årsmöte kan upphäva avstängning och uteslutning.

            <h4>§8 Styrelsen</h4>
            Styrelsen ansvarar för föreningens medlemslista, bidragsansökningar, medlemsvärvning, beslut som tas på årsmöten och övrig verksamhet.<br>
            Föreningens styrelse består av ordförande, vice ordförande, kassör och sekreterare. Vid behov kan även extra ledamöter väljas. Samma person får inte ha flera poster i styrelsen.<br>
            Styrelsen väljs på årsmöte och tillträder direkt efter valet. Valbar är medlem i föreningen.

            <h4>§9 Revisorer</h4>
            För granskning av föreningens räkenskaper och förvaltning väljs på årsmöte en eller två revisorer. Valbar är person som inte sitter i styrelsen. Revisor behöver inte vara medlem i föreningen.

            <h4>§10 Valberedning</h4>
            För att ta fram förslag på personer till de i stadgarna föreskrivna valen kan årsmötet välja en eller flera valberedare. Valbar är medlem i föreningen.

            <h4>§11 Ordinarie årsmöte</h4>
            <p>Ordinarie årsmöte ska hållas senast den 31 mars varje år. Styrelsen beslutar om tid och plats. För att vara behörigt måste föreningens medlemmar meddelas personligen (via brev, mail eller sms) minst två veckor i förväg.</p>
            Följande ärenden ska alltid behandlas på ordinarie årsmöte:
            <ul>
                <li>) mötets öppnande</li>
                <li>) mötets behörighet</li>
                <li>) val av mötets ordförande</li>
                <li>) val av mötets sekreterare</li>
                <li>) val av två personer att justera protokollet</li>
                <li>) styrelsens verksamhetsberättelse för förra året</li>
                <li>) ekonomisk berättelse för förra året</li>
                <li>) revisorernas berättelse för förra året</li>
                <li>) ansvarsfrihet för förra årets styrelse</li>
                <li>) motioner</li>
                <li>) årets verksamhetsplan</li>
                <li>) årets budget och fastställande av medlemsavgift</li>
                <li>) val av årets styrelse</li>
                <li>) val av årets revisor</li>
                <li>) val av årets valberedare</li>
                <li>) övriga frågor</li>
                <li>) mötets avslutande</li>
            </ul>

            <h4>§12 Extra årsmöte</h4>
            Om styrelsen vill, eller revisor eller minst en fjärdedel av föreningens medlemmar kräver det ska styrelsen kalla till extra årsmöte. Vid giltigt krav på extra årsmöte kan den som krävt det sköta kallelsen.<br>
            För att vara behörigt måste föreningens medlemmar meddelas minst två veckor i förväg. På extra årsmöte kan bara de ärenden som nämnts i kallelsen behandlas.

            <h4>§13 Firmateckning</h4>
            Rätten att skriva avtal i föreningens namn, föreningens firma, tecknas av ordförande, vice ordförande och kassör var för sig. Om särskilda skäl föreligger kan annan person utses att teckna föreningens firma.

            <h4>§14 Rösträtt</h4>
            Endast fullt betalande närvarande medlem har rösträtt på årsmöte. På styrelsemöten har endast närvarande ur styrelsen rösträtt.

            <h4>§15 Röstetal</h4>
            Alla frågor som behandlas på årsmöte eller styrelsemöte avgörs med enkel röstövervikt om inget annat står i stadgarna. Nedlagda röster räknas ej. Varje person med rösträtt har en röst. Vid lika röstetal får lottning avgöra.

            <h4>§16 Stadgeändring</h4>
            Dessa stadgar kan bara ändras på ordinarie eller extra årsmöte. För att vara giltig måste ändringen antas med två tredjedelar av antalet röster.<br>
            Då stadgeändring ska ske måste förslaget delges medlemmarna i kallelsen till mötet. I annat fall måste ändringen antas enhälligt.<br>
            Ändring av föreningens stadgar om föreningsform (§3), syfte (§4), stadgeändringar (§16) och upplösning (§17) kräver beslut på ordinarie årsmöte.

            <h4>§17 Upplösning</h4>
            Förslag om föreningens upplösning får endast framläggas på årsmöte. Att upplösning ska behandlas måste framgå av kallelsen. Föreningen kan inte upplösas så länge minst fem medlemmar vägrar godkänna upplösningen.<br>
            Vid upplösning ska föreningens skulder betalas. Därefter ska föreningens tillgångar gå till verksamhet i enlighet med föreningens syfte. Hur detta ska ske beslutas på det sista årsmötet.<br>
            En ansvarig ska utses för att avsluta föreningens konton, sköta överföring av tillgångar samt avregistrering från förbundet.
